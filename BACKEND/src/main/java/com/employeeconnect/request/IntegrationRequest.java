package com.employeeconnect.request;


public class IntegrationRequest {

    private String employee_id;
    private String company_email_id;
    private String direct_manager_employee_id;
    private String first_name;
    private String middle_name;
    private String last_name;
    private String business_unit;
    private String hod;
    
    
    public IntegrationRequest() {}


	public String getEmployee_id() {
		return employee_id;
	}


	public void setEmployee_id(String employee_id) {
		this.employee_id = employee_id;
	}


	public String getCompany_email_id() {
		return company_email_id;
	}


	public void setCompany_email_id(String company_email_id) {
		this.company_email_id = company_email_id;
	}


	public String getDirect_manager_employee_id() {
		return direct_manager_employee_id;
	}


	public void setDirect_manager_employee_id(String direct_manager_employee_id) {
		this.direct_manager_employee_id = direct_manager_employee_id;
	}


	public String getFirst_name() {
		return first_name;
	}


	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}


	public String getMiddle_name() {
		return middle_name;
	}


	public void setMiddle_name(String middle_name) {
		this.middle_name = middle_name;
	}


	public String getLast_name() {
		return last_name;
	}


	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}


	public String getBusiness_unit() {
		return business_unit;
	}


	public void setBusiness_unit(String business_unit) {
		this.business_unit = business_unit;
	}


	public String getHod() {
		return hod;
	}


	public void setHod(String hod) {
		this.hod = hod;
	}
	
	
	
    
    
}
